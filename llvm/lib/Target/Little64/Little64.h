// ISD node types used during lowering.
// CALL, RET_FLAG, and BRCC are generated by TableGen; pull in their enum
// (ordered alphabetically by TableGen).  The generated file opens
// namespace llvm::Little64ISD itself, so this must be at file scope.
#define GET_SDNODE_ENUM
#include "Little64GenSDNodeInfo.inc"

// All target ISD nodes (BRCC, CALL, RET_FLAG, Wrapper) are now generated
// by TableGen — no hand-written extension enum needed.

namespace llvm {

class Little64TargetMachine;
class FunctionPass;

FunctionPass *createLittle64ISelDag(Little64TargetMachine &TM);
FunctionPass *createLittle64CallDebugLocFixPass();

// Pass initialization functions — defined via INITIALIZE_PASS in their
// respective .cpp files; declared here so TargetMachine.cpp can call them.
void initializeLittle64AsmPrinterPass(PassRegistry &);
void initializeLittle64CallDebugLocFixPass(PassRegistry &);
void initializeLittle64DAGToDAGISelLegacyPass(PassRegistry &);

// Target operand flags for MachineOperand::getTargetFlags().
// These communicate relocation semantics from ISel lowering to MCInstLower.
namespace Little64II {
enum TOF : unsigned {
  MO_None   = 0,
  MO_GOT    = 1,  // GOT-indirect access (@got specifier)
  MO_TPREL  = 2,  // TLS TP-relative offset (@tprel specifier)
  MO_GOTTPREL = 3,  // TLS IE via GOT (@gottprel specifier)
};
} // namespace Little64II

// Condition codes used with Little64ISD::BRCC.
// The values map directly to the immediate operand of the BRCC pseudo.
namespace Little64CC {
enum CondCode : unsigned {
  EQ  = 0,  // JUMP.Z  (Z set)
  NE  = 1,  // invert EQ: JUMP.Z to false block
  LT  = 2,  // JUMP.LT (sign bit set after subtraction)
  GE  = 3,  // invert LT
  GT  = 4,  // JUMP.GT (!Z && !sign)
  LE  = 5,  // invert GT
  ULT = 6,  // JUMP.C  (carry/borrow set)
  UGE = 7,  // invert ULT
  UGT = 8,  // JUMP.C with swapped operands (B < A)
  ULE = 9,  // invert UGT
};
} // namespace Little64CC

} // namespace llvm

#endif // LLVM_LIB_TARGET_LITTLE64_LITTLE64_H
