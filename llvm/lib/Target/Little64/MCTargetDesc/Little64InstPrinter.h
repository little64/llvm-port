#ifndef LLVM_LIB_TARGET_LITTLE64_MCTARGETDESC_LITTLE64INSTPRINTER_H
#define LLVM_LIB_TARGET_LITTLE64_MCTARGETDESC_LITTLE64INSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class Little64InstPrinter : public MCInstPrinter {
public:
  Little64InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                      const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                    const char *Modifier = nullptr);
  void printLSRegOffset(const MCInst *MI, int OpNo, raw_ostream &O);
  void printMemOperand(const MCInst *MI, int OpNo, raw_ostream &O);
  void printPCRelOperand(const MCInst *MI, uint64_t Address, int OpNo,
                         raw_ostream &O);

  // Autogenerated by tblgen.
  std::pair<const char *, uint64_t>
  getMnemonic(const MCInst &MI) const override;
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  void printCustomAliasOperand(const MCInst *MI, uint64_t Address,
                               unsigned OpIdx, unsigned PrintMethodIdx,
                               raw_ostream &O);
  static const char *getRegisterName(MCRegister Reg);
  void printRegName(raw_ostream &OS, MCRegister Reg) override;
};

} // namespace llvm

#endif // LLVM_LIB_TARGET_LITTLE64_MCTARGETDESC_LITTLE64INSTPRINTER_H
